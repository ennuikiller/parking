- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
	
	
	NSDictionary *dict = [self.user.availableParking objectAtIndex:indexPath.row];
	
	//if ([[dict valueForKey:@"userid"] isEqualToString:self.user.userName]) return nil;
	
	
	
	NSLog(@"row is now %d",indexPath.row);
	NSLog(@"available parking = %d",[self.user.availableParking count] + 1);
    
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:CellIdentifier] autorelease];
    }
    
	//NSDictionary *dict = [self.user.availableParking objectAtIndex:indexPath.row];
	
	NSLog(@"location = %@",[dict objectForKey:@"location"]);
	NSMutableString *detailString =  [NSMutableString stringWithFormat:@"%.2g miles, ",[[dict objectForKey:@"distance"] floatValue]];
	NSString *details = [NSString stringWithString:@""];
	details = [detailString stringByAppendingFormat:@"%@, %@ points",[dict objectForKey:@"userid"], [dict objectForKey:@"points"]];
	
	cell.detailTextLabel.text = details;
	cell.textLabel.text = [dict objectForKey:@"location"];
    // Set up the cell...
	
    return cell;
}


